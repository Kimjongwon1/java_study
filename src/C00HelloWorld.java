public class C00HelloWorld {
    public static void main(String[] args) {
        System.out.println("hello world");
//      문자열을 출력 할 때는 " " 감싸기
//      숫자는 그냥
        int a = 20;
        System.out.println(a);
        String my_string = "hello world";
        System.out.println(my_string);
//      문자열과 숫자를 합하면 = 문자
        System.out.println(my_string+a);

    }
}
